def issue_score(it):
    """重要度スコア = 影響額(正規化)*0.5 + 緊急度*0.3 + 横断性*0.2（固定式）。"""
    max_amt = max(i["impact"] for i in ISSUES) or 1
    amt_n = it["impact"] / max_amt * 5
    return round(amt_n * WEIGHTS["影響額"] + it["urgency"] * WEIGHTS["緊急度"]
                 + it["cross"] * WEIGHTS["横断性"], 2)


def severity(it):
    """スコア帯から深刻度ラベル（高/中/低）。"""
    s = issue_score(it)
    if s >= 3.0:
        return "高"
    if s >= 2.0:
        return "中"
    return "低"


def ranked_issues():
    return sorted(ISSUES, key=issue_score, reverse=True)


